package FirstStep;

public class CharAddition {
    public static void main(String[] args) {
        char ch1 = 'A'; // ASCII value 65
        char ch2 = 'B'; // ASCII value 66
        int result = ch1 + ch2;
        System.out.println(result); // Output: 131




        String str = "Hello";
        char ch = '!';
        String res= str + ch;
        System.out.println(res); // Output: Hello!
    }
}
